//    Если светофор красный и машин нет- вывести "стой все рано".
//    Если светофор красный - и машины есть вывести "стой и жди".
//    Если светофор в аварийном режиме вывести "делай что хочешь"!


light = prompt("введи свет светофора");
let isRoadClear = confirm('шумахеры на дороге есть?');
if ((light==='green' || light==='зеленый' || light==='зелений') && isRoadClear===false){
    console.log('иди');
    document.write('иди');
    alert('иди');
    document.write("<br>");

}
else if ((light==='green' || light==='зеленый' || light==='зелений') && isRoadClear===true){
    console.log('подожди пока нарушители проедут');
    document.write('подожди пока нарушители проедут');
    alert('подожди пока нарушители проедут');
    document.write("<br>");

}
else if ((light==='желтый' || light==='жовтий' || light==='yellow') && isRoadClear===false){
    console.log('жди');
    document.write('жди');
    alert('жди');
    document.write("<br>");

}
else if ((light==='желтый' || light==='жовтий' || light==='yellow') && isRoadClear===true){
    console.log('все рано жди');
    document.write('все рано жди');
    alert('все рано жди');
    document.write("<br>");

}
else if ((light==='Red' || light==='Красный' || light==='Червоний') && isRoadClear===false){
    console.log('стой все рано');
    document.write('стой все рано');
    alert('стой все рано');
    document.write("<br>");

}
else if ((light==='Red' || light==='Красный' || light==='Червоний') && isRoadClear===true){
    console.log('стой и жди');
    document.write('стой и жди');
    alert('стой и жди');
    document.write("<br>");

}
else{
    console.log('делай что хочешь');
    document.write('делай что хочешь');
    alert('делай что хочешь');
    document.write("<br>");

}
